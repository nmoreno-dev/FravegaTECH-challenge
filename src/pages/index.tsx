import Head from "next/head";
import { useListGithubUsers } from "../querys/useGitHubUsers.query";
import UserCard from "../components/UserCard";
import Searcher from "../components/Search";
import { Grid2 } from "@mui/material";

export default function Home() {
  // Hook para listar todos los usuarios si no hay b√∫squeda activa
  const {
    data: usersData,
    isLoading: isLoadingList,
    isError: isErrorList,
  } = useListGithubUsers();

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div>
        <Searcher />

        {isLoadingList && <h1>Cargando datos...</h1>}

        {/* Mostrar error si lo hay */}
        {isErrorList && <h1>Error al obtener los usuarios</h1>}

        {/* Mostrar la lista de usuarios, completa o filtrada */}
        {!isLoadingList && !isErrorList && usersData && (
          <main>
            <Grid2 container>
              {usersData.map((user) => (
                <Grid2
                  key={user.id}
                  size={{ xs: 12, sm: 6, md: 4, lg: 3, xl: 2 }}
                >
                  <UserCard user={user} />
                </Grid2>
              ))}
            </Grid2>
          </main>
        )}
      </div>
    </>
  );
}
